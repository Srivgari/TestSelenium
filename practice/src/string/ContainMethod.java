package string;

public class ContainMethod {
    /*Contain() method searches the sequence of character in the given string. it returns true if the sequence of character found otherwise it returns false.
     * 
     */
	public static void main(String[] args)
	{
		String s="My name is Garima";
		System.out.println(s.contains("name"));
		System.out.println(s.contains("srivastava"));
		System.out.println(s.contains("My name"));
		
	}

}
